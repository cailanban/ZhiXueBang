## try-catch-finally机制

## 概念理解
try-catch-finally是Java中用于异常处理的核心机制。try块用于包裹可能抛出异常的代码；catch块用于捕获并处理特定类型的异常；finally块则包含无论是否发生异常都必须执行的代码（如资源释放）。该机制将正常逻辑与错误处理逻辑分离，提高代码的健壮性和可读性。

## 原理剖析
当try块中的语句抛出异常时，Java虚拟机会立即终止当前执行流程，并在调用栈中查找匹配的catch块。catch块按照声明顺序依次匹配异常类型，一旦匹配成功，则执行该catch块中的处理代码。如果所有catch块均不匹配，异常会沿调用栈向上传播。finally块无论try块是否抛出异常、是否被捕获，甚至遇到return语句，都会在方法返回前执行（除非JVM退出）。finally块通常用于清理资源，如关闭文件流、数据库连接等。

## 实例演示
```java
FileInputStream fis = null;
try {
    fis = new FileInputStream("test.txt");
int data = fis.read();
System.out.println((char) data);
} catch (FileNotFoundException e) {
    System.out.println("文件未找到: " + e.getMessage());
} catch (IOException e) {
    System.out.println("读取错误: " + e.getMessage());
} finally {
    if (fis != null) {
        try {
            fis.close();
} catch (IOException e) {
            e.printStackTrace();
}
    }
}
```
该示例演示了如何安全地读取文件，并在finally中确保流被关闭。

## 应用场景
- 文件I/O操作：无论读写成功与否，都需要关闭文件流。
- 数据库连接：确保连接、Statement、ResultSet被正确关闭。
- 网络通信：Socket、HTTP连接等资源释放。
- 锁操作：在finally中释放锁，避免死锁。
- 事务管理：在finally中回滚或提交事务。

