## switch-case 多分支

## 概念理解
switch-case 是 C 语言中用于实现多分支选择控制的结构。它根据一个整数表达式的值，从多个 case 标签中选择一个匹配的分支执行，从而替代冗长的 if-else if 链。其核心思想是将多路分支的判断简化为一次计算和跳转，使代码更清晰、高效。

## 原理剖析
switch 语句的执行过程基于跳转表（jump table）或二分查找实现，具体取决于编译器和 case 值的分布。当表达式的值计算后，程序会直接跳转到匹配的 case 标签处执行，而不是依次比较每个条件。如果没有匹配的 case，则执行 default 分支（如果存在）。每个 case 分支后通常需要 break 语句来跳出 switch 结构，否则会发生“穿透”（fall-through），即继续执行下一个 case 的代码，直到遇到 break 或 switch 结束。

## 实例演示
```c
#include <stdio.h>
int main() {
    int score = 85;
switch(score / 10) {
        case 10:
        case 9:
            printf("优秀\\n");
break;
case 8:
            printf("良好\\n");
break;
case 7:
            printf("中等\\n");
break;
case 6:
            printf("及格\\n");
break;
default:
            printf("不及格\\n");
}
    return 0;
}
```
该例根据分数段输出等级，多个 case 共享同一代码块（如 case 10 和 9）。

## 应用场景
switch-case 适用于以下场景：
- 菜单选择：根据用户输入的选项执行不同操作。
- 状态机：根据当前状态进行不同处理。
- 枚举值判断：对有限的、离散的整数值进行分支。
- 字符分类：根据字符类型（数字、字母、符号）执行不同逻辑。
- 简单计算器：根据运算符执行加减乘除。

