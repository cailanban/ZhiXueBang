## 散列表(Hash Table)

## 概念理解
散列表（Hash Table，又称哈希表）是一种根据键（Key）直接访问值（Value）的数据结构。它通过一个哈希函数将键映射到数组中的一个位置（称为槽或桶），从而实现近乎O(1)的平均查找、插入和删除操作。散列表的核心思想是“以空间换时间”，即用较大的连续存储空间换取极快的访问速度。

## 原理剖析
散列表的工作原理包括三个关键环节：
1. **哈希函数**：将任意大小的输入键转换为固定范围的整数索引。理想哈希函数应计算简单、分布均匀，减少冲突。常见方法有除留余数法（key % p）、平方取中法、折叠法等。
2. **冲突处理**：当两个不同的键映射到同一位置时，即发生冲突。解决冲突的主要方法有：
   - **开放地址法**：线性探测、二次探测、双重哈希等，顺序查找下一个空位。
   - **链地址法**：每个槽位维护一个链表，冲突的键链入同一链表。
3. **负载因子**：负载因子 = 已存储元素数 / 总槽位数。负载因子过高会显著增加冲突概率和查找时间，通常当负载因子超过阈值（如0.75）时触发动态扩容（rehashing）。

## 实例演示
假设我们有一个大小为7的散列表，哈希函数为 `hash(key) = key % 7`。依次插入键值：50, 21, 58, 17, 15, 49。
- 50 % 7 = 1 → 槽1
- 21 % 7 = 0 → 槽0
- 58 % 7 = 2 → 槽2
- 17 % 7 = 3 → 槽3
- 15 % 7 = 1 → 冲突！使用链地址法，将15链接到槽1的链表末尾。
- 49 % 7 = 0 → 冲突！将49链接到槽0的链表末尾。
最终散列表为：
槽0: 21 → 49
槽1: 50 → 15
槽2: 58
槽3: 17
槽4: 空
槽5: 空
槽6: 空
查找15时，计算hash=1，遍历链表两次找到，效率较高。

## 应用场景
散列表广泛应用于需要快速查找、插入和删除的场景：
- **数据库索引**：如MySQL的哈希索引，用于等值查询。
- **缓存系统**：如Redis、Memcached，键值对存储。
- **编译器符号表**：管理变量和函数名。
- **密码学**：存储密码哈希值（配合盐值）。
- **去重与计数**：如统计文章单词频率。
- **路由表**：快速查找IP地址对应的下一跳。

