## 变量与常量

## 概念理解
变量和常量是C语言中最基本的数据存储单元。变量是指在程序运行过程中其值可以改变的量，而常量是指在程序运行过程中其值保持不变的量。变量需要先定义后使用，定义时指定数据类型和变量名，系统会为其分配内存空间。常量则直接以字面形式出现，或通过#define、const关键字定义。

## 原理剖析
变量在内存中占据一块连续的存储单元，其大小由数据类型决定（如int占4字节，char占1字节）。变量名实质上是该存储单元起始地址的符号别名，通过变量名访问即通过地址读写数据。常量则直接存储在代码段或数据段，程序运行时不能修改。const修饰的变量本质上是“只读变量”，仍占用内存，但编译器禁止对其赋值；#define定义的宏常量在预处理阶段直接替换文本，不占用内存。

## 实例演示
```c
#include <stdio.h>
#define PI 3.14159  // 宏常量
int main() {
    int radius = 5;          // 变量定义并初始化
    const double pi = 3.14;   // const常量
    double area = PI * radius * radius;  // 使用变量和常量
    printf("半径=%d, 面积=%.2f\\n", radius, area);
radius = 10;  // 修改变量值
    area = pi * radius * radius;
printf("半径=%d, 面积=%.2f\\n", radius, area);
return 0;
}
```
输出：半径=5, 面积=78.54；半径=10, 面积=314.00

## 应用场景
变量用于存储程序运行过程中的临时数据，如计数器、用户输入、计算结果等。常量用于定义固定值，如圆周率、数组大小、状态码等，提高代码可读性和可维护性。在大型工程中，常将常量集中定义在头文件中，方便统一修改。

