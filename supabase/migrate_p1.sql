## 线性表的定义与操作

## 概念理解
线性表是数据结构中最基本、最常用的一种线性结构。它是由n（n≥0）个相同类型的数据元素组成的有限序列。当n=0时，称为空表。线性表中的元素之间存在一对一的线性关系，即除第一个元素外，每个元素有且仅有一个直接前驱；除最后一个元素外，每个元素有且仅有一个直接后继。这种关系使得数据元素在逻辑上排列成一条“直线”。

## 原理剖析
线性表在计算机中的存储方式主要有两种：顺序存储和链式存储。顺序存储使用一组连续的存储单元依次存储线性表的元素，物理位置与逻辑顺序一致，支持随机访问，但插入和删除操作需要移动大量元素。链式存储通过指针将元素链接起来，每个节点包含数据域和指针域，物理位置不连续，插入和删除灵活，但访问元素需要从头遍历。线性表的基本操作包括初始化、销毁、判断空表、求长度、按位查找、按值查找、插入、删除、遍历输出等。这些操作的时间复杂度取决于存储结构：顺序表随机访问O(1)，插入删除O(n)；链表随机访问O(n)，插入删除O(1)（已知位置）。

## 实例演示
