## 递归与分治思想

## 概念理解
递归与分治是线性结构中两种紧密关联的思想。递归是指一个函数直接或间接调用自身，将原问题转化为规模更小的同类子问题；分治则是一种“分而治之”的策略，将一个大问题分解为若干相互独立且与原问题相似的子问题，分别求解后再合并结果。两者常结合使用：分治是宏观策略，递归是实现手段。

## 原理剖析
递归的核心在于两个要素：**递归基（终止条件）**和**递归关系（递推公式）**。递归基确保问题在最小规模时直接求解，不再调用自身；递归关系则将当前问题转化为更小规模的问题。分治原理遵循三步：**分解（Divide）**——将原问题拆分成多个子问题；**求解（Conquer）**——递归地解决每个子问题；**合并（Combine）**——将子问题的解组合成原问题的解。递归与分治的效率通常由递推关系决定，如T(n)=aT(n/b)+f(n)，可通过主定理分析时间复杂度。

