## break 与 continue

## 概念理解
break和continue是C语言中用于控制循环流程的两个关键字。break用于立即终止当前所在的循环（for、while、do-while）或switch语句，程序跳出循环体继续执行后续代码。continue则用于跳过当前循环迭代中剩余的语句，直接进入下一次循环的条件判断（for循环会先执行迭代表达式）。

## 原理剖析
从底层看，break和continue都是通过改变程序计数器（PC）的跳转目标来实现的。编译器在生成机器码时，break对应一条无条件跳转指令，将PC直接指向循环结束后的地址；continue则对应跳转到循环条件判断或迭代语句的地址。在嵌套循环中，break只跳出其所在的最内层循环，而continue也只作用于当前循环层。

## 实例演示
```c
// break示例：查找数组中第一个负数
int arr[] = {3, 5, -1, 7};
for(int i = 0; i < 4; i++) {
    if(arr[i] < 0) {
        printf("找到负数：%d\\n", arr[i]);
break;  // 找到后立即退出循环
    }
}

// continue示例：打印1~10中的奇数
for(int i = 1; i <= 10; i++) {
    if(i % 2 == 0) {
        continue;  // 跳过偶数
    }
    printf("%d ", i);
}
// 输出：1 3 5 7 9
