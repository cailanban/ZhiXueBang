## 基础排序（冒泡/选择/插入）

## 概念理解
基础排序算法是计算机科学中最基本的排序方法，包括冒泡排序、选择排序和插入排序。它们通过不同的策略将无序序列调整为有序序列，是理解更复杂排序算法的基础。

## 原理剖析
- **冒泡排序**：重复遍历序列，比较相邻元素，若顺序错误则交换，每轮将最大（或最小）元素“冒泡”到末尾。时间复杂度O(n²)，稳定排序。
- **选择排序**：每轮从未排序部分选出最小（或最大）元素，放到已排序部分的末尾。时间复杂度O(n²)，不稳定排序。
- **插入排序**：将未排序元素逐个插入到已排序部分的正确位置。时间复杂度O(n²)，稳定排序，在数据接近有序时效率较高。

## 实例演示
以序列[5, 3, 8, 4, 2]升序排序为例：
- **冒泡排序**：第一轮比较5和3交换得[3,5,8,4,2]，再比较5和8不交换，8和4交换得[3,5,4,8,2]，8和2交换得[3,5,4,2,8]；后续轮次类似，最终得到[2,3,4,5,8]。
- **选择排序**：第一轮找出最小值2，与第一个元素5交换得[2,3,8,4,5]；第二轮从剩余[3,8,4,5]中找出最小值3，已在首位不动；第三轮找出最小值4与8交换得[2,3,4,8,5]；第四轮找出最小值5与8交换得[2,3,4,5,8]。
- **插入排序**：将第一个元素5视为已排序；第二个元素3插入到5前得[3,5]；第三个元素8插入到5后得[3,5,8]；第四个元素4插入到3和5之间得[3,4,5,8]；第五个元素2插入到最前得[2,3,4,5,8]。

## 应用场景
- **冒泡排序**：教学演示、小规模数据排序。
- **选择排序**：当交换操作代价高时（如移动大型记录），因为交换次数少。
- **插入排序**：数据基本有序或数据量较小（如<50），常作为高级排序（如快速排序）的辅助排序。

