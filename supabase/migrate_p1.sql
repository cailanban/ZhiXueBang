## if-else 分支结构

## 概念理解
if-else 是 C 语言中最基本的分支结构，用于根据条件表达式的真假决定程序的执行路径。其核心思想是“如果条件成立，则执行某段代码；否则执行另一段代码”。这种结构使得程序能够根据不同的输入或状态做出不同的响应，是实现程序逻辑判断的基础。

## 原理剖析
if-else 语句的执行流程如下：首先计算条件表达式（通常是一个关系表达式或逻辑表达式），若结果为真（非零值），则执行 if 后面的语句块；若结果为假（零值），则执行 else 后面的语句块。注意，else 是可选的，可以单独使用 if。在 C 语言中，条件表达式的结果是整型值，任何非零值都被视为真，零被视为假。if-else 可以嵌套使用，形成多分支判断，但嵌套层次过深会降低代码可读性，建议使用 else if 链或 switch 结构替代。

## 实例演示
```c
#include <stdio.h>
int main() {
    int score = 85;
if (score >= 60) {
        printf("及格\\n");
} else {
        printf("不及格\\n");
}
    return 0;
}
```
此例中，score=85 满足条件 score>=60，因此输出“及格”。若 score=45，则输出“不及格”。

## 应用场景
if-else 广泛应用于：用户登录验证（判断密码是否正确）、成绩等级划分（根据分数输出 A/B/C/D）、权限控制（根据角色显示不同菜单）、输入数据有效性检查（如年龄是否在合理范围）等。它是实现程序决策逻辑的核心工具。

