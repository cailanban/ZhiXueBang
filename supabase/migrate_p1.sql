## for/while/do-while 循环

## 概念理解
循环结构是C语言中用于重复执行某段代码的控制结构。for、while和do-while是三种基本循环语句。for循环适用于已知循环次数的情况，它包含初始化、条件判断和循环变量更新三个部分。while循环先判断条件，条件为真时执行循环体，适用于循环次数不确定但需要前置判断的场景。do-while循环先执行一次循环体，再判断条件，因此至少执行一次，适用于需要先执行一次再判断的场景。

## 原理剖析
循环执行依赖于条件表达式和循环体的执行流程。for循环的语法为：for(初始化; 条件; 更新) { 循环体 }，执行顺序为：初始化→条件判断→循环体→更新→条件判断……。while循环的语法为：while(条件) { 循环体 }，先判断条件，若为真则执行循环体，然后再次判断。do-while循环的语法为：do { 循环体 } while(条件);，先执行循环体，再判断条件，若为真则继续循环。三者均可通过break语句提前退出循环，通过continue语句跳过本次循环的剩余部分进入下一次迭代。注意：条件表达式必须能最终变为假，否则形成死循环；循环体内应包含改变条件的语句。

## 实例演示
示例1：使用for循环打印1到5。
```c
for(int i=1; i<=5; i++) {
    printf("%d ", i);
}
```
输出：1 2 3 4 5
示例2：使用while循环累加1到100。
```c
int sum=0, i=1;
while(i<=100) {
    sum += i;
i++;
}
printf("%d", sum);
```
输出：5050
示例3：使用do-while循环实现至少输入一次验证。
```c
int num;
do {
    printf("输入一个正数: ");
scanf("%d", &num);
} while(num <= 0);
```

## 应用场景
for循环常用于遍历数组、迭代固定次数（如打印乘法表）。while循环适用于基于条件控制的循环，如读取文件直到结束、用户输入直到特定值。do-while循环适用于需要至少执行一次的场景，如菜单显示、密码验证。

