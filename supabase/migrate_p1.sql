## 栈的定义与应用

## 概念理解
栈（Stack）是一种操作受限的线性表，只允许在一端（称为栈顶，Top）进行插入和删除操作，另一端（称为栈底，Bottom）固定。这种限制决定了栈具有“后进先出”（Last In First Out, LIFO）的特性，即最后进入栈的元素最先被取出。栈可以形象地类比为一摞盘子：我们总是从顶部取走盘子，也总是将新盘子放在顶部。

## 原理剖析
栈的核心操作包括：
- 压栈（Push）：将元素放入栈顶，栈顶指针上移。
- 弹栈（Pop）：移除栈顶元素，栈顶指针下移。
- 查看栈顶（Peek/Top）：获取栈顶元素的值但不移除。
- 判空（IsEmpty）：检查栈中是否还有元素。

栈的实现方式主要分为两种：
1. **顺序栈**：使用数组实现，通过一个整型变量（top）标记栈顶位置。优点：访问速度快；缺点：需要预先分配固定大小，可能造成空间浪费或溢出。
2. **链式栈**：使用单链表实现，以头结点作为栈顶。优点：动态扩容，空间利用率高；缺点：需要额外的指针存储开销。

无论哪种实现，所有操作的时间复杂度均为O(1)，这是栈高效性的关键。

