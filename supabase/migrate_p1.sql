## 时间复杂度与空间复杂度

## 概念理解
时间复杂度和空间复杂度是衡量算法效率的两个核心指标。时间复杂度描述算法执行时间随输入规模增长的变化趋势，空间复杂度描述算法运行时所需内存空间随输入规模增长的变化趋势。两者通常用大O符号（Big O notation）表示，忽略常数因子和低阶项，关注最坏情况下的增长量级。

## 原理剖析
时间复杂度基于基本操作（如赋值、比较、算术运算）的执行次数，而非实际运行时间。例如，一个循环执行n次，每次执行常数操作，则时间复杂度为O(n)。空间复杂度包括固定空间（如指令、常量、变量）和可变空间（如动态分配、递归栈）。递归算法的空间复杂度通常与递归深度成正比。分析时需关注输入规模n，常见复杂度从低到高为：O(1)、O(log n)、O(n)、O(n log n)、O(n²)、O(2ⁿ)。

## 实例演示
例1：线性查找。在长度为n的数组中查找某元素，最坏情况下需比较n次，时间复杂度O(n)，空间复杂度O(1)（仅用几个变量）。
例2：归并排序。对n个元素排序，递归深度log n，每层合并需O(n)时间，总时间复杂度O(n log n)；递归调用需额外数组存储，空间复杂度O(n)。

## 应用场景
- 算法选型：处理大数据集时优先选择低复杂度算法（如快速排序O(n log n)优于冒泡排序O(n²)）。
- 系统设计：内存受限的嵌入式系统需严格控制空间复杂度（如用原地算法）。
- 性能优化：通过分析复杂度定位瓶颈，例如将双重循环改为哈希表查找，从O(n²)降为O(n)。

