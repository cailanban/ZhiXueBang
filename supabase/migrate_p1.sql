- Push(3)：栈为 [1, 2, 3] (3在栈顶)
- Pop()：返回3，栈变为 [1, 2]
- Peek()：返回2，栈不变
- Pop()：返回2，栈变为 [1]
- IsEmpty()：返回false，因为还有元素1

