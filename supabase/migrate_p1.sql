## 实例演示
```java
class Animal {
    public void sound() {
        System.out.println("动物发出声音");
}
}

class Dog extends Animal {
    @Override
    public void sound() {
        System.out.println("狗叫：汪汪");
}
}

public class Test {
    public static void main(String[] args) {
        Animal a = new Dog();  // 父类引用指向子类对象
